//Program demonstrujuci vypis roznych literalov.
#include<iostream>
using namespace std;

int main()
{
    //Vypis celociselnych literalov
    cout << 282 << endl;
    cout << -18 << endl;
    cout << 921 << "  " << 58 << endl;

    //Vypis realnych literalov
    cout << 45.12 << endl;
    cout << -256.23 << endl;

    //Vystup znakovych literalov
    cout << 'A' << endl;

    //Vystup retazcovych literalov
    cout << "Programujeme v jazyku C++" << endl;
    cout << "Programujeme\nv jazyku C++" << '\n';

    cout << "Vysledok vypoctu je " << 78 << endl;


    return 0;
}
